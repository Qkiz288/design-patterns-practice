package facade.model;

public class PopcornPopper {

    public void on() {
        System.out.println("Popcorn popper is ON");
    }

    public void off() {
        System.out.println("Popcorn popper is OFF");
    }

    public void pop() {
        System.out.println("Popcorn popper is popping fresh popcorn!");
    }

}
